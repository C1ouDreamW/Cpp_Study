void solve() {
  int n;
  cin >> n;
  vector<int> a(n);
  for (int i = 0; i < n; i++)
    cin >> a[i];
  sort(a.begin(), a.end());
  for (int i = 1; i < a.size(); i++) {
    if (a[i] == a[i - 1]) {
      cout << "NO" << endl;
      return;
    }
  }
  cout << "YES" << endl;
}

